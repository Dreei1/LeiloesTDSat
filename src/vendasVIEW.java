     public void listarProdutos(){
     
           String status = "Vendido";
            ProdutosDAO produtosdao = new ProdutosDAO();
            List<ProdutosDTO> listaVenda = produtosdao.listarProdutosVendidos(status);
            DefaultTableModel model = (DefaultTableModel) tblVendas.getModel();
            model.setNumRows(0);
            tblVendas.setRowSorter(new TableRowSorter(model));
            
            for(ProdutosDTO p : listaVenda){
                Object[] obj = new Object[] {
                    p.getId(),
                    p.getNome(),
                    p.getValor(),
                    p.getStatus(),
                };
                model.addRow(obj);
            }
    }
    
